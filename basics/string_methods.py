# capitalize()
my_string = "welcome to india. we are happy to welcome here"
print(my_string.capitalize())

#casefold() or lower()
my_string2 = "HELLO WORLD"
print(my_string2.casefold())

#center()
print(my_string2.center(40))

# islower(): True if all charcters in the string is lower.
my_string3= "hello"
print(f"islower: {my_string3.islower()}")

# isupper(): True if all charcters in the string is upper.
my_string4= "HELLO"
print(f"isupper: {my_string4.isupper()}")

# isdigit() : True, if all characters are numbers in the string
my_strnums = "123456677"
print(f"isdigit: {my_strnums.isdigit()}")

