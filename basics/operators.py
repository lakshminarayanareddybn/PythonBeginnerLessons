## Operators
### To perform any operations on variables and values
### we need operators

## Example, to add two numbers we need +. here + is an operator.

# print(10+2)

# print("10"+"2")

## Python has many operators
## Arithmetic operators : + (Addition), -(Substraction), *(Multiplication), /(Division), %(Modulus), **(Exponentiation), //(Floor division)
## Assignment operators : =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<=
## Comparision operators : == (Equal), !=(Not Equal), > (Greater than), < (Less than), <= (Less than or equal to), >= (Greater than or equal to)
## Logical operators: and, or, not 
## Identity operators: in, not in
## Bitwise operators : & (AND), | (OR), ^ (XOR), ~ (NOT), << (LEFT SHIFT), >> (RIGHT SHIFT)

## Arithmetic operators examples
x = 6
y = 5

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)
print(x**y)
print(x//y)
