## shift operators

## bitwise right shift (>>) : Divide
## bitwise left shift (<<) : Multiplication

a = 10
print(bin(a))

right_shift_a = a << 1
print(f"{right_shift_a:b}", end="\t")
print(right_shift_a)

right_shift_a = right_shift_a << 1
print(f"{right_shift_a:b}", end="\t")
print(right_shift_a)

right_shift_a = right_shift_a << 1
print(f"{right_shift_a:b}", end="\t")
print(right_shift_a)

right_shift_a = right_shift_a << 1
print(f"{right_shift_a:b}", end="\t")
print(right_shift_a)
