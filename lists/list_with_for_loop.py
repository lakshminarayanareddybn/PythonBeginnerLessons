## For loop
### for loop is used to iterate over a sequence.
### sequence can be a list, a tuple, a string, set, dictionary

for item in ["banana", "orange", "apple", "guava"]:
    print(item)

print("++++++++++++++++++++++++++++++")

for item in list(("banana", "orange", "apple", "guava")):
    print(item)

print("++++++++++++++++++++++++++++++")

fruits = ["banana", "orange", "apple", "guava"]

for item in fruits:
    print(item)

cities = ["bangalore", "chennai", "delhi"]
for item in cities:
    print(item)
